print("Essa é uma 'string' (str).")  # Na programação, tudo que abre fecha, ex: "" () {}
print('Essa é uma "string" (str).')
print("Essa é uma \"string\" (str).")  # Forma errada
print("Esse é um \"texto\" (str).")  # Essa forma não funciona
print('Esse é um \'texto\' (str).')  # \escape ele anula as aspas iguais
print('Essa é uma  \n (str).' )  #  \n quebra de linha
print(r'Essa é uma  \n (str).' )  # r antes das aspas mostar o codigo inteiro
print(r'Esse é um \'Aspas aqui\' (str).')  # r na frente mostra o codigo inteiro com as aspas no meio, modo errado


